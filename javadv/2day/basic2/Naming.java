package basic2;

public class Naming {
	public static void main(String[] args) {
		boolean aBCD;
		byte 가나다;
		short _abcd;
		char $ab_cd;
		// int 3abcd; 숫자는 이름 맨 앞에 올 수 없음
		long abcd3;
		// float int; 저바 예약어는 사용할 수 없음
		double main;
		// int my Work; // 스페이스, 특수 키는 사용할 수 없음
		String myClassName;
		int ABC;
		
		// 상수
		final double PI;
		final int MY_DATA;
		final float myData; // 소문자 사용은 권장하지 않음
	}
}

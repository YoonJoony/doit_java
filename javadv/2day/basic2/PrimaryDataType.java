package basic2;

public class PrimaryDataType {
	public static void main(String[] args) {
		boolean bool1 = true;
		boolean bool2 = false;
		System.out.println(bool1);
		System.out.println(bool2);
		System.out.println();
		
		byte value1 = 10;
		short value2 = -10;
		int value3 = 100;
		long value4 = -100L;
		System.out.println(value1);
		System.out.println(value2);
		System.out.println(value3);
	}
}
